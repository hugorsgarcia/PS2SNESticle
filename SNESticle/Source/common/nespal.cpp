///////////////////////////////////////////////////////////////////////////////
// _NesPalColorEmphasisChannel
//
// Dim a single color channel (R/G/B) by 25% across all palette entries.
// channel: 0=R, 1=G, 2=B (byte offset within Color32T)
///////////////////////////////////////////////////////////////////////////////

static void _NesPalColorEmphasisChannel(Color32T* pal, unsigned int count, unsigned char channel)
{
	Uint8* ptr = ((Uint8*)pal) + channel;

	for (unsigned int i = 0; i < count; i++)
	{
		*ptr = (Uint8)((*ptr * 3) >> 2);
		ptr += 4;
	}
}

///////////////////////////////////////////////////////////////////////////////
// _NesPalColorEmphasis
//
// Apply NES color emphasis. Emphasis bits indicate which channels to
// emphasize (keep bright); the OTHER channels get dimmed by 25%.
// emphasis bits: bit 0 = emphasize R, bit 1 = emphasize G, bit 2 = emphasize B
///////////////////////////////////////////////////////////////////////////////

static void _NesPalColorEmphasis(Color32T* pal, unsigned int count, unsigned char emphasis)
{
	// Invert bits: emphasis bits say what to keep, we need to dim the rest
	emphasis = (emphasis & 0xFF) ^ 0x7;

	if (emphasis & 1)
		_NesPalColorEmphasisChannel(pal, count, 0); // dim R
	if (emphasis & 2)
		_NesPalColorEmphasisChannel(pal, count, 1); // dim G
	if (emphasis & 4)
		_NesPalColorEmphasisChannel(pal, count, 2); // dim B
}

///////////////////////////////////////////////////////////////////////////////
// _NesPalMonochrome
//
// Apply NES monochrome/greyscale mode. Each color is replaced by the
// first color in its row of 16 (the $x0 column = grey/luma reference).
///////////////////////////////////////////////////////////////////////////////

static void _NesPalMonochrome(Color32T* pal, unsigned int count)
{
	for (unsigned int i = 0; i < count; i++)
	{
		pal[i] = pal[i & 0xF0];
	}
}

///////////////////////////////////////////////////////////////////////////////
// NesPalComposePalette
//
// Compose a palette with emphasis and monochrome effects applied.
// iPal: bits 0-2 = emphasis mode, bit 3 = monochrome
// Palette: destination buffer (at least 64 Color32T entries)
// BasePal: source base palette (64 entries)
// brightness: number of colors to process (clamped to 64)
///////////////////////////////////////////////////////////////////////////////

void NesPalComposePalette(int iPal, Color32T* Palette, Color32T* BasePal, unsigned int brightness)
{
	// Clamp brightness to max 64 colors
	if (brightness > 64)
		brightness = 64;

	// Copy base palette
	memcpy(Palette, BasePal, brightness * sizeof(Color32T));

	// Apply color emphasis if any emphasis bits are set
	if (iPal & 0x7)
		_NesPalColorEmphasis(Palette, brightness, (unsigned char)(iPal & 0x7));

	// Apply monochrome if bit 3 is set
	if (iPal & 0x8)
		_NesPalMonochrome(Palette, brightness);
}

///////////////////////////////////////////////////////////////////////////////
// NesPalGetStockPalette
//
// Return pointer to a built-in stock NES palette.
///////////////////////////////////////////////////////////////////////////////

Color32T* NesPalGetStockPalette(NesPalE eNesPal)
{
	switch (eNesPal)
	{
	case NESPAL_SHADY:
		return _NesPal_Shady;
	case NESPAL_CCOVELL:
	default:
		return _NesPal_CCovell;
	}
}

